void SMS_EExec ( char* apPath ) {

 __asm__ __volatile__(
  ".set noreorder\n\t"
  "move     $s0, $a0\n\t"
  "jal      SMS_IOPReset\n\t"
  "addiu    $a0, $zero, 1\n\t"
  "lui      $a0, 0x0100\n\t"
  "la       $a1, s_Loader\n\t"
  "jal      memcpy\n\t"
  "li       $a2, %0\n\t"
  "jal      FlushCache\n\t"
  "li       $a0, 0\n\t"
  "jal      FlushCache\n\t"
  "li       $a0, 2\n\t"
  "la       $a1, _exec_error\n\t"
  ".set reorder\n\t"
  "lui      $ra, 0x0100\n\t"
  "subu     $sp, $ra, 16\n\t"
  "move     $a0, $s0\n\t"
  :: "n"(  sizeof ( s_Loader )  )
 );

}  /* end SMS_EExec */

