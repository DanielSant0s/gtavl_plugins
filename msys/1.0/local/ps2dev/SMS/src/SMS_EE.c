char* SMS_ReverseString ( char* apStr, int aLen ) {

 char* lpFirst = apStr;
 char* lpLast  = apStr + aLen;

 for ( ; lpFirst != lpLast && lpFirst != --lpLast; ++lpFirst ) {

  char lChr = *lpFirst;

  *lpFirst = *lpLast;
  *lpLast  = lChr;

 }  /* end for */

 return apStr;

}  /* end SMS_ReverseString */

